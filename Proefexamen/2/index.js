
/*Schrijf een programma dat twee variabelen met waarden heeft (bijvoorbeeld a = 5
en b = 10), en de waarden van de variabelen wisselt zonder een derde variabele te
gebruiken. Toon de nieuwe waarden van a en b in de console.
 */


let a = 5;
let b = 10;

a = a + b; // Nu is a gelijk aan 15 (5 + 10)
b = a - b; // Nu is b gelijk aan 5 (15 - 10)
a = a - b; // Nu is a gelijk aan 10 (15 - 5)

console.log("Nieuwe waarde van a: " + a); // a = 10
console.log("Nieuwe waarde van b: " + b); // b = 5





